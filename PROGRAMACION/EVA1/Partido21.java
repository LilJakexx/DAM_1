public class Partido21{ 
	public static void main(String[] args) {
		int numeroEntero = 5; 
		float numeroConDecimales = 5;
		char Caracter = '5';
		boolean Booleano = true;
		String Texto = "Cinco";
		String otroTexto = "5";
		final int variableDefinitiva = 5;
		System.out.println("5");
		//AHORA IMPRIMIMOS LOS DATOS		
		System.out.println("Variable INT (entero)"+numeroEntero);
		System.out.println("Variable float (decimales)"+numeroConDecimales);
		System.out.println("Variable char (caracter) "+Caracter);
		System.out.println("Variable boolean (Booleano) "+Booleano);
		System.out.println("Variable String (texto) "+Texto);
		System.out.println("Variable String (otro texto con números)"+otroTexto);
		System.out.println("Variable final (final) "+variableDefinitiva);	
	}
}
